package com.excel;

public class Pattern6 {
	/* a a a a a
	 * b b b b b
	 * c c c c c
	 * d d d d d
	 * e e e e e
	 */
	
	public static void main(String[] args)
	{
		char i;
		char j;
		for(i='a'; i<='e'; i++)
		{
			for(j='a'; j<='e'; j++)
			{
				System.out.print(i + "");
				
				
			}
			System.out.println();
		}
	}
}
